#include "libft.h"

t_list	*ft_lstnew(int content)
{
	t_list	*begin_pointer;

	begin_pointer = (t_list *)malloc(sizeof(t_list));
	if (begin_pointer == (NULL))
		return (begin_pointer);
	begin_pointer->value = content;
	begin_pointer->next = (NULL);
	return (begin_pointer);
}
